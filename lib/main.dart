}

class _MyHomePageState extends State<MyHomePage> {


  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('Test App'),
      ),
      body: Center(
        child: Container(
          color: Colors.black54,
          height: MediaQuery.of(context).size.height * 0.35,
          child: androidVideoMediaPlayer(
              link: 'https://readymedia.moderntv.cloud:8092/distribute/readymedia/URBANTV_stream_wt/5-20-hls/live.m3u8', seekTo: '0', isStream: true),
        ),
      ),
    );
  }



  Widget androidVideoMediaPlayer(
      {String link, String seekTo, bool isStream = false}) {
    const viewType = 'view1';

    final creationParams = <String, dynamic>{
      'link': link,
      'seekTo': seekTo,
      'isStream': isStream
    };

    return AndroidView(
      viewType: viewType,
      layoutDirection: TextDirection.ltr,
      creationParams: creationParams,
      creationParamsCodec: const StandardMessageCodec(),
    );
  }
}
